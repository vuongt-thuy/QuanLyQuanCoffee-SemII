    private void bntEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bntEditMouseClicked
        int indexSelected = tblListUser.getSelectedRow();

        if (indexSelected == -1) {
            new ErrorsMessageDialog().setMessage("Please choose value!");
        } else {
            EditUser edit = new EditUser();
            edit.setData(listUser.get(indexSelected));
            edit.setVisible(true);
        }
    }//GEN-LAST:event_bntEditMouseClicked

    private void setDataTable(List<User> list) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

        modelUser.setRowCount(0);
        for (User u : list) {
            modelUser.addRow(new Object[]{
                u.getUsername(), u.getFullname(), u.getTimework(), u.getPhone(), u.getLevel_id() == 0 ? "Quản lý" : "Nhân viên", dateFormat.format(u.getCreated_at()), u.isStatus() ? "" : "Disable"
            });
        }
    }

