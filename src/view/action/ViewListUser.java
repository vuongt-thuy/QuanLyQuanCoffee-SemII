    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        String keySearch = txtKeySearch.getText();
        List<User> currenList = new ArrayList<>();

        if (keySearch.isEmpty()) {
            new ErrorsMessageDialog().setMessage("Invalid value!");
        } else {
            listUser = userDAO.selectByName(keySearch);
            if (listUser.size() == 0) {
                User user = userDAO.selectByUsername(keySearch);
                if (user != null) {
                    listUser.add(user);
                }
            }
            setDataTable(listUser);
        }
    }//GEN-LAST:event_btnSearchMouseClicked

