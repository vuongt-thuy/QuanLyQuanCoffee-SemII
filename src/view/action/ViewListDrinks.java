    private void setDataTable(List<Product> list) {
        Locale localeVN = new Locale("vi", "VN");
        NumberFormat vn = NumberFormat.getInstance(localeVN);

        modelDrinkList.setRowCount(0);
        listCategory = catDAO.getListDrinks();

        for (Product p : list) {
            modelDrinkList.addRow(new Object[]{
                p.getId(), p.getName(), p.getImage(), vn.format(p.getPrice()), (int) p.getDiscount() == 0 ? "" : (int) p.getDiscount() + "%", vn.format(p.getSale_price())
            });
        }
    }

