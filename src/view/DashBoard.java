    private void comboxFoodsItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboxFoodsItemStateChanged

        if (evt.getStateChange() == evt.SELECTED) {
            if (comboxFoods.getSelectedItem().toString().equals("All Menu")) {
                listProductFoods = proDAO.getListFoods();
                setDataTableMenu(modelMenuFood, listProductFoods);
            } else {
                listCategoryFood = catDAO.getListFoods();
                int indexSelected = comboxFoods.getSelectedIndex();
                int cat_id = listCategoryFood.get(indexSelected - 1).getId();

                listProductFoods.clear();
                listFoods = proDAO.getListFoods();
                for (Product p : listFoods) {
                    if (p.getCat_id() == cat_id) {
                        listProductFoods.add(p);
                    }
                }
                setDataTableMenu(modelMenuFood, listProductFoods);
            }
        }
    }//GEN-LAST:event_comboxFoodsItemStateChanged

    private void btnAddFoodsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddFoodsMouseClicked
        // add product to the bill

        int indexselected = tblMenuFoods.getSelectedRow();

        if (indexselected == -1) {
            new ErrorsMessageDialog().setMessage("Please choose food!");
        } else {
            // check list order (procduct is exist?)
            int count = 0;
            int i = 0;
            int updateQty = 0;
            int currentQty = 0;

            Product p = listProductFoods.get(indexselected);
            String proID = p.getId();
            String proName = p.getName();
            String image = p.getImage();
            float price = p.getSale_price();

            setOrderId();
            OrderDetails od = new OrderDetails();
            od.setOrder_id(orderID);
            od.setPro_id(proID);
            od.setPro_name(proName);
            od.setQuantity(1);
            od.setPrice(price);

            for (OrderDetails o : listOrderDetails) {
                if (o.getPro_id().equals(proID)) {
                    currentQty = Integer.parseInt(tblCurrentOrder.getModel().getValueAt(i, 2).toString());
                    updateQty = currentQty + 1;
                    od.setQuantity(updateQty);
                    listOrderDetails.remove(i);
                    listOrderDetails.add(od);
                    count++;
                }
                i++;
            }

            if (count != 1) {
                listOrderDetails.add(od);
            }

            showListOrder(listOrderDetails);
            calculateTotalBill();
        }
    }//GEN-LAST:event_btnAddFoodsMouseClicked

    private void showListOrder(List<OrderDetails> listOrderDetails) {
        List<Product> listProduct = proDAO.getList();
        modelOrder.setRowCount(0);
        for (OrderDetails o : listOrderDetails) {
            for (Product p : listProduct) {
                if (o.getPro_id().equals(p.getId())) {
                    modelOrder.addRow(new Object[]{
                        o.getPro_name(), p.getImage(), o.getQuantity(), vn.format(o.getPrice())
                    });
                }
            }
        }
    }
    
    private void calculateTotalBill() {
        float subtotal = 0;
        for (OrderDetails o : listOrderDetails) {
            subtotal += o.getPrice() * o.getQuantity();
        }

        txtSubtotal.setText(vn.format(subtotal) + " VNĐ");
    }
    
    private void setOrderId() {
        listOrder = orderDAO.getList();
        if (listOrder.size() == 0) {
            orderID = 100000;
        } else {
            orderID = listOrder.get(listOrder.size() - 1).getId() + 1;
        }
    }

